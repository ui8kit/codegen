export function indent(text: string, level: number, unit = "  "): string {
  const pad = unit.repeat(level);
  return text
    .split("\n")
    .map((line) => (line.trim() === "" ? line : pad + line))
    .join("\n");
}

export function pascalCase(kebab: string): string {
  return kebab
    .split(/[-_.]/)
    .filter(Boolean)
    .map((s) => s.charAt(0).toUpperCase() + s.slice(1))
    .join("");
}

export function lowerFirst(name: string): string {
  return name.charAt(0).toLowerCase() + name.slice(1);
}

export function jsString(value: string): string {
  return JSON.stringify(value);
}

/** Recipe variable/const name from the variants.json file stem. */
export function recipeVarName(recipeFile: string): string {
  const stem = recipeFile.replace(/\.variants\.json$/, "");
  return pascalCase(stem) + "Variants";
}
